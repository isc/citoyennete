// Banque alignée sur les listes officielles des questions de connaissance de
// l'examen civique, publiées par le ministère de l'Intérieur sur
// formation-civique.interieur.gouv.fr. Deux niveaux :
//   - CSP (carte de séjour pluriannuelle) — 191 énoncés publics, niveau A2
//   - CR (carte de résident / naturalisation) — 209 énoncés publics, niveau B2
//
// 32 énoncés sont communs aux deux listes. Au total la banque contient 368
// questions, chacune taguée par les niveaux où elle apparaît (`levels`).
//
// Les énoncés sont repris à l'identique du site officiel. Les 4 choix et la
// bonne réponse — que le ministère ne publie pas — sont rédigés à partir du
// Livret du citoyen, de la Constitution de 1958, du Code civil/pénal et de
// service-public.fr.
//
// L'examen réel = 40 QCM (28 connaissance + 12 mises en situation
// confidentielles), 45 min max, seuil 80 % (32/40).
//
// Statut : premier jet. Les questions où la bonne réponse est ambiguë sont
// listées dans BANK_REVIEW.md à la racine du repo.

export type Category =
  | 'principes'
  | 'institutions'
  | 'droits'
  | 'histoire'
  | 'societe';

export type ExamLevel = 'csp' | 'cr';

export interface Question {
  id: string;
  category: Category;
  prompt: string;
  choices: string[];
  answerIndex: number;
  /** Source courte de la bonne réponse (livret, constitution, code, etc.). */
  source: string;
  /** Page du Livret du citoyen quand applicable. */
  page?: number;
  explanation?: string;
  /**
   * Niveaux d'examen où l'énoncé apparaît officiellement.
   * Par défaut `['cr']` (la banque historique vise la naturalisation).
   */
  levels?: ExamLevel[];
}

